package ac.kr.ft.com.dto;

public class ComDTO {
	private String ctrR_cnt;
	private String community_cnt;
	private String visitor_cnt;
	private String seq;
	private String contents;
	private String reg_dt;
	private String table_name;
	private String kind;
	
	public String getVisitor_cnt() {
		return visitor_cnt;
	}

	public void setVisitor_cnt(String visitor_cnt) {
		this.visitor_cnt = visitor_cnt;
	}

	public String getSeq() {
		return seq;
	}

	public void setSeq(String seq) {
		this.seq = seq;
	}

	public String getContents() {
		return contents;
	}

	public void setContents(String contents) {
		this.contents = contents;
	}

	public String getReg_dt() {
		return reg_dt;
	}

	public void setReg_dt(String reg_dt) {
		this.reg_dt = reg_dt;
	}

	public String getTable_name() {
		return table_name;
	}

	public void setTable_name(String table_name) {
		this.table_name = table_name;
	}

	public String getCommunity_cnt() {
		return community_cnt;
	}

	public void setCommunity_cnt(String community_cnt) {
		this.community_cnt = community_cnt;
	}

	public String getCtrR_cnt() {
		return ctrR_cnt;
	}

	public void setCtrR_cnt(String ctrR_cnt) {
		this.ctrR_cnt = ctrR_cnt;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}
	
	
}
