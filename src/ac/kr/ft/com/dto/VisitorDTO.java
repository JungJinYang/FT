package ac.kr.ft.com.dto;

public class VisitorDTO {
	private String today;
	private String visitor_cnt;
	private String reg_user_no;
	private String reg_dt;
	private String chg_user_no;
	private String chg_dt;
	private String to_code;
	
	public String getToday() {
		return today;
	}
	public void setToday(String today) {
		this.today = today;
	}
	public String getVisitor_cnt() {
		return visitor_cnt;
	}
	public void setVisitor_cnt(String visitor_cnt) {
		this.visitor_cnt = visitor_cnt;
	}
	public String getReg_user_no() {
		return reg_user_no;
	}
	public void setReg_user_no(String reg_user_no) {
		this.reg_user_no = reg_user_no;
	}
	public String getReg_dt() {
		return reg_dt;
	}
	public void setReg_dt(String reg_dt) {
		this.reg_dt = reg_dt;
	}
	public String getChg_user_no() {
		return chg_user_no;
	}
	public void setChg_user_no(String chg_user_no) {
		this.chg_user_no = chg_user_no;
	}
	public String getChg_dt() {
		return chg_dt;
	}
	public void setChg_dt(String chg_dt) {
		this.chg_dt = chg_dt;
	}
	public String getTo_code() {
		return to_code;
	}
	public void setTo_code(String to_code) {
		this.to_code = to_code;
	}
}
